CREATE PROCEDURE SP_GET_CUSTOMER (
    P_ID INTEGER
)
RETURNS (
    O_NAME VARCHAR(100),
    O_EMAIL VARCHAR(200)
)
AS
BEGIN
    SELECT NAME, EMAIL
    FROM TEST_CUSTOMER
    WHERE ID = :P_ID
