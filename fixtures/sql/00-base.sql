-- Base fixture for DBeaver Firebird UI tests
-- Creates the test database schema with objects needed by the test suite.

-- Tables
CREATE TABLE TEST_CUSTOMER (
    ID INTEGER NOT NULL PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(200),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE TEST_ORDER (
    ID INTEGER NOT NULL PRIMARY KEY,
    CUSTOMER_ID INTEGER NOT NULL REFERENCES TEST_CUSTOMER(ID),
    AMOUNT DECIMAL(15,2) NOT NULL,
    ORDER_DATE DATE DEFAULT CURRENT_DATE
);

-- Sequence
CREATE SEQUENCE SEQ_CUSTOMER_ID;
CREATE SEQUENCE SEQ_ORDER_ID;

-- View
CREATE VIEW V_TEST_CUSTOMER_ORDERS AS
SELECT c.ID, c.NAME, COUNT(o.ID) AS ORDER_COUNT, COALESCE(SUM(o.AMOUNT), 0) AS TOTAL
FROM TEST_CUSTOMER c
LEFT JOIN TEST_ORDER o ON o.CUSTOMER_ID = c.ID
GROUP BY c.ID, c.NAME;

